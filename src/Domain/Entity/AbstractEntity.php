<?php

namespace Mzm\TaxCalculator\Domain\Entity;

abstract class AbstractEntity
{
    protected $id;

    /**
     * Get the value of id
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set the value of id
     *
     * @return  self
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }
}
